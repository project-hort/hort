//! Hard-coded sample lists for PyPI and npm.
//!
//! Context: the sandbox allowlist for this run admits `pypi.org`,
//! `registry.npmjs.org`, and `crates.io`, but not the popularity-ranking
//! hosts we'd normally query (hugovk.github.io top-pypi-packages,
//! api.npmjs.org download-counts, pypistats, libraries.io). For cargo we
//! page through `crates.io/api/v1/crates?sort=downloads` which is
//! allowlisted; for PyPI and npm we fall back to a curated list of
//! top-downloaded packages, drawn from the publicly-known popularity
//! rankings as of early 2026. The exact ranks are not load-bearing — the
//! goal is to pull a representative mix of small/medium/large metadata
//! blobs, not to recreate the head of the download distribution.
//!
//! These lists are deliberately kept modest (~200 entries each) so the
//! tool can run in a few minutes per format at the default per-request
//! delay. Scale via the `--sample-size` flag.

